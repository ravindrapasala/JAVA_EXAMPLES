package com.Strings_Concept;

public class ToLowerCase {

	public static void main(String[] args) {
		
		String str = "delhi";
		String str1 = "DELHI";
		
		String str2 = str1.toLowerCase();
		
		
		if(str.equals(str2))
		{
			System.out.println("Both are equal");
		}
		else
		{
			System.out.println("Both are not equal");
		}
		
		
		
		
	}

}
