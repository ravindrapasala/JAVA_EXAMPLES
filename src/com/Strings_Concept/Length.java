package com.Strings_Concept;

public class Length {

	public static void main(String[] args) {
		
		String str = "Test";
		
		System.out.println("No.of characters: "+str.length());
		
		for(int i = 0; i<str.length(); i++)
		{
			System.out.println(str.charAt(i));
		}
		
		

	}

}
