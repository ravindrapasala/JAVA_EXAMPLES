package com.Strings_Concept;

public class CharAt {

	public static void main(String[] args) {
		
		String str ="ravi";
		
		System.out.println(str.charAt(2));
		
		System.out.println("#########################################");
		
		for(int i = 3; i<str.length(); i++)
		{
			System.out.println(str.charAt(i));
			
		}
		
	}

}
